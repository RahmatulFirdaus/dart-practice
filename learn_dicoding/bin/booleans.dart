void main(){
  bool alwaysTrue = true;
  var alwaysFalse = false;

  /**
   * Penggunaan ! pada boolean ( atau disebut not/bang)
   * berfungsi untuk membalikkan nilai 
   */
  bool notTrue = !true; //output false
  var notFalse = !false; // output true

  print(alwaysTrue);
  print(alwaysFalse);
  print(notTrue);
  print(notFalse);
}